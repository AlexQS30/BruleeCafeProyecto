  /**
     * Método que se activa al presionar el botón de registrar.
     * Recolecta los datos de la UI y los pasa a la fachada.
     */
    private void registrar() {
        String correo = txtCorreo.getText().trim();
        String pass = String.valueOf(txtPass.getPassword());
        String nom = txtNombre.getText().trim();
        String rol = cbxRol.getSelectedItem().toString();

        // Validación de campos vacíos (corregida con && y .isEmpty())
        if (nom.isEmpty() || correo.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
            return; // Detiene la ejecución si hay campos vacíos.
        }

        // La UI delega la lógica de registro a la fachada.
        boolean registroExitoso = facade.registrarNuevoUsuario(nom, correo, pass, rol);

        if (registroExitoso) {
            JOptionPane.showMessageDialog(this, "Usuario registrado con éxito. Ahora puede iniciar sesión.", "Registro Completado", JOptionPane.INFORMATION_MESSAGE);
            // Abre la ventana de login y cierra la actual.
            LoginUI iniciar = new LoginUI();
            iniciar.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Ocurrió un error durante el registro. Por favor, intente de nuevo.", "Error de Registro", JOptionPane.ERROR_MESSAGE);
        }
    }

